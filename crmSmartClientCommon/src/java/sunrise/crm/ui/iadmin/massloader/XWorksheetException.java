public class XWorksheetException extends RuntimeException {
	public XWorksheetException() {
	}

	public XWorksheetException(String message) {
		super(message);
	}

	public XWorksheetException(String message, Throwable cause) {
		super(message, cause);
	}

	public XWorksheetException(Throwable cause) {
		super(cause);
	}

}
