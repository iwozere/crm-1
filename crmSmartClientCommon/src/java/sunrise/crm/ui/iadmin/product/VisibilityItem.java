package sunrise.crm.ui.iadmin.product;

public class VisibilityItem {

    private String continueCode;
    private String isAttribute;
    private String objid;
    private String visible;
    private String deactivateCode;
    private String isLineItem;
    private String activateCode;
    private String isCatalog;
    private String name;

    public String getContinueCode() {
        return continueCode;
    }

    public void setContinueCode(String continueCode) {
        this.continueCode = continueCode;
    }

    public String getIsAttribute() {
        return isAttribute;
    }

    public void setIsAttribute(String isAttribute) {
        this.isAttribute = isAttribute;
    }

    public String getObjid() {
        return objid;
    }

    public void setObjid(String objid) {
        this.objid = objid;
    }

    public String getVisible() {
        return visible;
    }

    public void setVisible(String visible) {
        this.visible = visible;
    }

    public String getDeactivateCode() {
        return deactivateCode;
    }

    public void setDeactivateCode(String deactivateCode) {
        this.deactivateCode = deactivateCode;
    }

    public String getIsLineItem() {
        return isLineItem;
    }

    public void setIsLineItem(String isLineItem) {
        this.isLineItem = isLineItem;
    }

    public String getActivateCode() {
        return activateCode;
    }

    public void setActivateCode(String activateCode) {
        this.activateCode = activateCode;
    }

    public String getIsCatalog() {
        return isCatalog;
    }

    public void setIsCatalog(String isCatalog) {
        this.isCatalog = isCatalog;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
